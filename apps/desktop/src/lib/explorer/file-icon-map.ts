const fileIconCache = new Map<string, string | null>();

export function getFileIconSvg(name: string): string | null {
  const lower = name.toLowerCase();
  const cached = fileIconCache.get(lower);
  if (cached !== undefined) return cached;

  const exact = exactFileToSvg[lower];
  if (exact) {
    const path = `${FILE_ICONS_BASE}/files/${exact}.svg`;
    fileIconCache.set(lower, path);
    return path;
  }

  const ext = longestSvgExt(lower);
  if (ext) {
    const base = extToSvg[ext];
    if (base) {
      const path = `${FILE_ICONS_BASE}/files/${base}.svg`;
      fileIconCache.set(lower, path);
      return path;
    }
  }

  fileIconCache.set(lower, null);
  return null;
}

function longestSvgExt(lowerName: string): string | null {
  const parts = lowerName.split(".").filter(Boolean);
  if (parts.length <= 1) return null;
  for (let i = 0; i < parts.length - 1; i++) {
    const ext = parts.slice(i).join(".");
    if (extToSvg[ext]) return ext;
  }
  return parts.at(-1) ?? null;
}

