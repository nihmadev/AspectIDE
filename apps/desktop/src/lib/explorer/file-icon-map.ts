const folderIconCache = new Map<string, string>();

export function getFolderIconSvg(folderName: string, _expanded?: boolean): string {
  const lower = folderName.toLowerCase().replace(/^\.+/, "");
  const cached = folderIconCache.get(lower);
  if (cached) return cached;

  const base = folderNameToSvg[lower];
  const path = base
    ? `${FILE_ICONS_BASE}/folders/folder-${base}.svg`
    : `${FILE_ICONS_BASE}/folders/folder.svg`;
  folderIconCache.set(lower, path);
  return path;
}
